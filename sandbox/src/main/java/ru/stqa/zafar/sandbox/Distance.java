package ru.stqa.zafar.sandbox;

public class Distance {

  public double x1;
  public double y1;
  public double x2;
  public double y2;

  public double p1;
  public double p2;

public Distance (double p1, double p2, double x1, double x2, double y1, double y2){

  this.x1 = x1;
  this.x2 = x2;
  this.y1 = y1;
  this.y2 = y2;
  this.p1 = p1;
  this.p2 = p2;

}


}
